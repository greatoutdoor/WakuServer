package waku.vo;

public class Company {
	int cNo;
	String cName;
	String cImage;
	String cPage;
	String cAd;
	
	public int getcNo() {
		return cNo;
	}
	public Company setcNo(int cNo) {
		this.cNo = cNo;
		return this;
	}
	public String getcName() {
		return cName;
	}
	public Company setcName(String cName) {
		this.cName = cName;
		return this;
	}
	public String getcImage() {
		return cImage;
	}
	public Company setcImage(String cImage) {
		this.cImage = cImage;
		return this;
	}
	public String getcPage() {
		return cPage;
	}
	public Company setcPage(String cPage) {
		this.cPage = cPage;
		return this;
	}
	public String getcAd() {
		return cAd;
	}
	public Company setcAd(String cAd) {
		this.cAd = cAd;
		return this;
	}
    	
	

}
