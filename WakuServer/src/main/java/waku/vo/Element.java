package waku.vo;

public class Element {
	int gNo;
	int iNo;
	int iReq;
	
	public int getgNo() {
		return gNo;
	}
	public Element setgNo(int gNo) {
		this.gNo = gNo;
		return this;
	}
	public int getiNo() {
		return iNo;
	}
	public Element setiNo(int iNo) {
		this.iNo = iNo;
		return this;
	}
	public int getiReq() {
		return iReq;
	}
	public Element setiReq(int iReq) {
		this.iReq = iReq;
		return this;
	}
}
